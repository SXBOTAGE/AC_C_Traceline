inline void wallhackLogic(){
    switch (setting.enableWallhack)
    {
    case true: WriteProcessMemory(process.handle,(LPVOID)WALLHACK_INJ,(LPCVOID)wallAllB,14,NULL); break;
    case false: WriteProcessMemory(process.handle,(LPVOID)WALLHACK_INJ,(LPCVOID)wallOriginB,20,NULL); break;
    }

    return;
}